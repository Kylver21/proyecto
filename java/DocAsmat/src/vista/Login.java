    private void btnIniciobtnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciobtnInicioActionPerformed
        String correo = txtCorreo.getText().trim();
        String contraseña = new String(txtContraseña.getPassword()).trim();

        // Verificar que el correo contenga el símbolo @
        if (!correo.contains("@")) {
            JOptionPane.showMessageDialog(this, "El correo debe contener el símbolo '@'.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verificar que la contraseña tenga al menos 8 caracteres
        if (contraseña.length() < 8) {
            JOptionPane.showMessageDialog(this, "La contraseña debe tener al menos 8 caracteres.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verificar las credenciales del usuario a través del controlador
        boolean autenticado = controlador.validarInicioSesion(correo, contraseña);
        if (autenticado) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            // Aquí puedes redirigir a la siguiente vista o cerrar la ventana de login
        } else {
            JOptionPane.showMessageDialog(this, "Credenciales incorrectas.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIniciobtnInicioActionPerformed

