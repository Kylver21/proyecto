    private void btnOlvideContraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOlvideContraActionPerformed

        btnOlvideContra.addActionListener(e -> {
            String emailReceptor = JOptionPane.showInputDialog("Ingrese su correo electrónico:");

            if (!emailReceptor.contains("@")) {
                JOptionPane.showMessageDialog(this, "El correo debe contener el símbolo '@'.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!emailReceptor.isEmpty() && emailReceptor.contains("@")) {
                CorreoRecuperacion correoRecuperacion = new CorreoRecuperacion();
                correoRecuperacion.generarCodigoVerificacion();
                String codigoVerificacion = correoRecuperacion.getCodigoVerificacion();
                String asunto = "Recuperación de contraseña - Código de verificación";
                String contenido = "Tu código de verificación es: " + codigoVerificacion + ". No lo compartas con nadie.";

                EnviarCorreo.enviarCorreo(emailReceptor, asunto, contenido);
                RecuperarContra recuperarContra = new RecuperarContra();
                recuperarContra.setLocationRelativeTo(null);
                recuperarContra.setVisible(true);
                this.dispose();

            }
        });

    }//GEN-LAST:event_btnOlvideContraActionPerformed

